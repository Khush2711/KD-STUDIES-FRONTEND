function printCourseDetails(course) {
    console.log("Course ID:", course._id);
    console.log("Course Name:", course.courseName);
    console.log("Instructor:", course.instructor);
    console.log("What You Will Learn:", course.whatYouWillLearn);
    console.log("Price:", course.price);
    console.log("Thumbnail:", course.thumbnail);
    console.log("Tags:", course.tag.join(", "));
    console.log("Instructions:", course.instructions.join(", "));
    
    console.log("\nCourse Content:");
    course.courseContent.forEach((section, index) => {
        console.log(`  Section ${index + 1}: ${section.sectionName} (ID: ${section._id})`);
        
        section.subSection.forEach((subSec, subIndex) => {
            console.log(`    Subsection ${subIndex + 1}:`);
            console.log(`      Title: ${subSec.title}`);
            console.log(`      Description: ${subSec.description}`);
            console.log(`      Video URL: ${subSec.videoUrl}`);
            console.log(`      Subsection ID: ${subSec._id}`);
        });
    });
}


